package com.gb.trace.entity.track;

import org.springframework.data.neo4j.core.schema.GeneratedValue;
import org.springframework.data.neo4j.core.schema.Id;
import org.springframework.data.neo4j.core.schema.Node;
import org.springframework.data.neo4j.core.schema.Relationship;

import java.util.Date;

@Node("Ship")
public class ShipTrackerEvent {
    @Id
    @GeneratedValue
    private Long id;
    private Long shipId;
    private String name;
    private String port;
    private Date date;
    private String programName;
    private String instanceId;
    private String traceId;
    private String spanId;
    private String parentSpanId;

    @Relationship(value = "PARENT", direction = Relationship.Direction.OUTGOING)
    private ShipTrackerEvent parentShipTrackerEvent;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getShipId() {
        return shipId;
    }

    public void setShipId(Long shipId) {
        this.shipId = shipId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getProgramName() {
        return programName;
    }

    public void setProgramName(String programName) {
        this.programName = programName;
    }

    public String getInstanceId() {
        return instanceId;
    }

    public void setInstanceId(String instanceId) {
        this.instanceId = instanceId;
    }

    public String getTraceId() {
        return traceId;
    }

    public void setTraceId(String traceId) {
        this.traceId = traceId;
    }

    public String getSpanId() {
        return spanId;
    }

    public void setSpanId(String spanId) {
        this.spanId = spanId;
    }

    public String getParentSpanId() {
        return parentSpanId;
    }

    public void setParentSpanId(String parentSpanId) {
        this.parentSpanId = parentSpanId;
    }

    public ShipTrackerEvent getParentShipTrackerEvent() {
        return parentShipTrackerEvent;
    }

    public void setParentShipTrackerEvent(ShipTrackerEvent parentShipTrackerEvent) {
        this.parentShipTrackerEvent = parentShipTrackerEvent;
    }
}
